(
	ID INT IDENTITY(1,1) PRIMARY KEY,
	HOTEN NVARCHAR(50) NOT NULL,
	NGAYSINH DATE NULL,
	GIOITINH BIT NULL,
	EMAIL NVARCHAR(50) NULL,
	SODT NCHAR(20) NULL,
	CMND NCHAR(20) NULL,
	DIACHI NVARCHAR(50) NULL,
	NGAYVAOLAM DATE NULL
)
GO
CREATE TABLE	 MUONTRASACH
(
	IDMUON INT IDENTITY(1,1) PRIMARY KEY,
