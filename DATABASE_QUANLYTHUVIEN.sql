GO
CREATE TABLE	 MANHINH
(
	MAMANHINH INT IDENTITY(1,1) PRIMARY KEY,
	TENMANHINH NVARCHAR(50) NOT NULL
)
GO
CREATE TABLE	 PHANQUYEN
(
	MAMANHINH INT NOT NULL,
