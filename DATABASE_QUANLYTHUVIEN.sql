(
	IDPHIEU INT IDENTITY(1,1) PRIMARY KEY,
	IDCHITIETMUON INT NOT NULL,
	IDTHUTHULAPPHIEU INT NOT NULL,
	NGAYLAPPHIEU DATE NOT NULL,
	SONGAYQUAHAN INT NOT NULL,
	TIENPHAT FLOAT NOT NULL,
	GHICHUPHAT NVARCHAR(50) NULL
