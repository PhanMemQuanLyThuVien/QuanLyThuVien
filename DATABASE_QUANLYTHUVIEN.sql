(
	IDCHITIETMUON INT IDENTITY(1,1) PRIMARY KEY,
	IDMUON INT NOT NULL,
	IDSACH INT NOT NULL,
	IDTHUTHUCHOMUON INT NOT NULL,
	IDTHUTHUNHANTRA INT NULL,
	SOLUONGMUON INT NOT NULL,
	NGAYMUON DATE NOT NULL,
	HANTRA DATE NULL,
	TIENTHECHAN FLOAT NOT NULL,
	TINHTRANG BIT NOT NULL
